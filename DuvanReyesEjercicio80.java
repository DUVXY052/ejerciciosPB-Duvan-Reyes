import java.util.Scanner;

public class DuvanReyesEjercicio80 {
    public static void main(String[] args) {
        Scanner leer=new Scanner(System.in);

        System.out.println("Ingrese un dato entero: ");
        int dtint=leer.nextInt();

        System.out.println("Ingrese un dato real: ");
        double ddoub=leer.nextDouble();

        System.out.println("Valor entero: " + dtint);
        System.out.println("Valor real: " + ddoub);
    }
}
